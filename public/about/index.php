            <h2>Ouxsoft Meaning</h2>
            <p>Ouxsoft is made of both the words “oux” and “soft.” The first word, “oux”, was considered because it is the last three letters of the founder, Matthew Heroux, last name. It was chosen because “Oux” is short for Ourococcus, which is a green genus of algae. The fundamental idea being that that small things coalescing over time have a big impact. The second word, “soft”, is short for the word “software” as that is what we make.</p>

            <h2>Logo Meaning</h2>
            <p>Each of the larger ellipses that form a circle in the logo are designed to be ourococcus. The eye-like structure is dual purpose, it is both the ourococcus nucleus and shaped like an eye to represent the eye and the evolution of perception. The eight ellipses are coalescing to form multicellular life. The number eight symbolizes balance, luck, and the ultimate form of achievement. </p>

